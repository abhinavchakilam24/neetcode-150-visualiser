// Build flat list of all problems from TOPICS
export const allProblems = TOPICS.flatMap(topic =>
  topic.problems.map(p => ({
    ...p,
    topic: topic.id,
    topicLabel: topic.label,
    topicIcon: topic.icon,
    data: PROBLEM_DATA[p.slug] || null,
  }))
);

export const allTopics = TOPICS;

export function getProblemBySlug(slug) {
  return PROBLEM_DATA[slug] || null;
}

export function getTopicById(id) {
  return TOPICS.find(t => t.id === id) || null;
}
